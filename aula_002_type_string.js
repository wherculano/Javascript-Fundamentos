const meuNome = "Goku"
const frase = "Oi, eu sou o "
// concatenacao de strings
console.log(frase + meuNome)

// funcoes de formatacao
const textoEmMinusculo = "Hello WORLD!"
console.log(textoEmMinusculo.toLowerCase())

const textoEmMMaiusculo = "hello javaScript!"
console.log(textoEmMMaiusculo.toUpperCase())

// tamanho da string
const senha = "m1Nh453Nh43h53gUr4"
console.log("Minha senha possui " + senha.length + " caracteres")

// substituindo caracteres
console.log("I'm studying JavaLanguage".replace("Language", "Script"))

// Template String
console.log(`Oi, eu sou o ${meuNome}!!`)


